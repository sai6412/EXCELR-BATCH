public class Day_5_loops {
    public static void main(String[] args) {
        String c ="classroom";
        System.out.println(c.substring(c.length() -3));//print last 3 letters or char of string
        System.out.println(c.substring(c.length() -1));//print last 1 letters or char of string
        //loops
        //for loop
        //for(initialize;condition;incrementation)
        for(int count=1;count<=10;count++){
            System.out.println("sai");
        }
        
    }
    
}
